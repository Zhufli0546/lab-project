package WaterQuality;

public class WQ {
	private String qua_id;
	private String code_name;
	private String longitude;
	private String latitude;
	private String qua_cntu;
	private String qua_cl;
	private float qua_ph;
	private String update_date;
	
	public String getQua_id() {
		return qua_id;
	}
	public void setQua_id(String qua_id) {
		this.qua_id = qua_id;
	}
	public String getCode_name() {
		return code_name;
	}
	public void setCode_name(String code_name) {
		this.code_name = code_name;
	}
	public String getLongitude() {
		return longitude;
	}
	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}
	public String getLatitude() {
		return latitude;
	}
	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}
	public String getQua_cntu() {
		return qua_cntu;
	}
	public void setQua_cntu(String qua_cntu) {
		this.qua_cntu = qua_cntu;
	}
	public String getQua_cl() {
		return qua_cl;
	}
	public void setQua_cl(String qua_cl) {
		this.qua_cl = qua_cl;
	}
	public float getQua_ph() {
		return qua_ph;
	}
	public void setQua_ph(float qua_ph) {
		this.qua_ph = qua_ph;
	}
	public String getUpdate_date() {
		return update_date;
	}
	public void setUpdate_date(String update_date) {
		this.update_date = update_date;
	}
}
